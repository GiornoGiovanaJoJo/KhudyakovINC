async def _call_yandex_gpt(system_prompt: str, user_text: str) -> str:
    if not API_KEY or not FOLDER_ID:
        raise HTTPException(status_code=500, detail="Ключи YandexGPT не настроены на сервере.")
        
    url = "https://llm.api.cloud.yandex.net/foundationModels/v1/completion"
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Api-Key {API_KEY}"
    }
    
    data = {
        "modelUri": f"gpt://{FOLDER_ID}/yandexgpt-lite/latest",
        "completionOptions": {
            "stream": False,
            "temperature": 0.3,
            "maxTokens": 1000
        },
        "messages": [
            {
                "role": "system",
                "text": system_prompt
            },
            {
                "role": "user",
                "text": user_text
            }
        ]
    }
    
    async with httpx.AsyncClient(timeout=60.0) as client:
        try:
            response = await client.post(url, headers=headers, json=data)
            response.raise_for_status()
            result = response.json()
            return result['result']['alternatives'][0]['message']['text']
        except Exception as e:
            raise HTTPException(status_code=500, detail=f"Ошибка YandexGPT API: {str(e)}")


@router.post("/evaluate", response_model=AIResponse)
async def evaluate_order(request: AIRequest, current_user=Depends(get_current_admin)):
    """Оценка стоимости проекта на базе сырого текста клиента"""
    text = await _call_yandex_gpt(SYSTEM_PROMPT_EVALUATE, request.text)
    return AIResponse(text=text)


